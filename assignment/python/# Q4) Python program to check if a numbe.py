# Q4) Python program to check if a number is positive, negative, or zero

num = int(input("Enter a number: "))

if num > 0:
    print("The number is Positive")
elif num < 0:
    print("The number is Negative")
else:
    print("The number is Zero")


# Q10) Check even or odd
